    public void cargarComprasGenerales(){
        try{
            //PREPARAR LA jTable
            String titulos[] = {"N¬∞","ID Compra","Id Proveedor","Fecha","Total"}; //Se definen los titulos que tendra la tabla
            comprasTableModel = new DefaultTableModel(null,titulos); //Se cargan los titulos a la tabla
            String fila [] = new String[5]; //Se define el tama√±o de columnas que tendra cada fila en la que se cargaran los datos

            //Crear un objeto de JTable para aplicar los hacks de ordenar :v
            JTable p = new JTable(comprasTableModel);

            //PREPARAR LA CONEXION A SQL Y EL OBJETO PARA EJECUTAR LA CONSULTA DE LA VISTA :v
            Statement smt=con.conectar().createStatement(); //Crea un objeto para poder hacer la consultar con ResultSet
            String consulta = "Select * from verComprasMP";
            ResultSet r = smt.executeQuery(consulta); //Es el equivalente a la wea que devuelve el metodo listar() :v
            //Empezar a cargar los datos---
            int c = 1;
            while(r.next()){
                fila[0] = String.valueOf(c) + "¬∞";
                //A partir de aqui empieza a cargar los datos de la base de datos ala jTable
                fila[1] = r.getString(1);
                fila[2] = r.getString(2);
                fila[3] = r.getString(3);
                fila[4] = r.getString(4);
                comprasTableModel.addRow(fila); //Con este codigo agrega toda una fila de datos, la cual representa los datos de un docente
                c++;
            }
            r.close();

            tablaComprasGenerales.setModel(comprasTableModel); //Carga el modelo a la tabla que se definio anteriormente
            //Weas que ordenan :v
            TableRowSorter<TableModel> ordena = new TableRowSorter<TableModel>(comprasTableModel);
            tablaComprasGenerales.setRowSorter(ordena);
            this.tablaComprasGenerales.setModel(comprasTableModel);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error al cargar los datos a la tabla: "+e.getMessage(),"ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
        }
    }//Cierre cargarComprasGenerales
    
