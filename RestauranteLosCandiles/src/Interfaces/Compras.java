    private void txtIDCompraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDCompraKeyTyped
         if(txtIDCompra.getText().length()<3){
            txtIDCompra.setText("COP");
        }
        if(txtIDCompra.getText().length()>=6){
            evt.consume();
        }
        String cara= String.valueOf(evt.getKeyChar());
        String nombrePattern="[\\d]";
        String cad;
        Pattern pattern = Pattern.compile(nombrePattern);
        Matcher matcher = pattern.matcher(cara);
        if(matcher.matches()==false){
            evt.consume();
        }
        else{
            cad=txtIDCompra.getText();
            if(cad.length()>=3&&cad.length()<=6){
                if(cad.length()!=3){
                    nombrePattern="^[COP]+[\\d]{1,3}";
                    pattern = Pattern.compile(nombrePattern);
                    matcher = pattern.matcher(cad)  ;
                    if(matcher.matches()==false){evt.consume();}
                }
            }
            else{evt.consume();}
        }
    }//GEN-LAST:event_txtIDCompraKeyTyped

